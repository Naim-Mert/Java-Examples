package java1.ödev;


public class Courses {

	private int id;
	private String name;
	private int weekCount;

	public Courses() {
		
	}
	
	public Courses(int id, String name, int weekCount) {
		super();
		this.id = id;
		this.name = name;
		this.weekCount = weekCount;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getWeekCount() {
		return weekCount;
	}

	public void setWeekCount(int weekCount) {
		this.weekCount = weekCount;
	}

	
	
	



}
